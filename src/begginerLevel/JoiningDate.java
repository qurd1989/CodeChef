package begginerLevel;

import java.util.Scanner;

public class JoiningDate {
    public static void main(String[] args) {

        Scanner sc = new Scanner(System.in);
        int t = sc.nextInt();
        while(t--> 0){
            int n = sc.nextInt();
            int k = sc.nextInt();
            // N candidates
            // N = A1 ->5, A2 ->5, ... An -> 5
            if (n == k) {
                System.out.println(0);
                continue;
            }
        }
    }
}
